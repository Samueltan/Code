package com.sample.basic.java8.methodreference.objectmethod;

class Car {
    private int id;
    private String color;

    public Car(int i) {
        id = i;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
